class Calculadora { //Los nombres de clases se inician con mayúscula.
    sumar(num1, num2) { //Método suma
        return num1 + num2;
    }
    restar(num1, num2) { //Método resta
        return num1 - num2;
    }
    multiplicar(num1, num2) { //Método multiplicación
        return num1 * num2;
    }
    dividir(num1, num2) { //Método división
        return num1 / num2;
    }
}